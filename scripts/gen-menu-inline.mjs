import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const dir = path.join(root, "src", "assets", "menu");
const ids = {
  "salt-pepper-prawns.jpg": "salt-pepper-prawns",
  "drunken-noodles.jpg": "drunken-noodles",
  "green-curry.jpg": "green-curry",
};

let body = "";
for (const [file, id] of Object.entries(ids)) {
  const b64 = fs.readFileSync(path.join(dir, file)).toString("base64");
  body += `  "${id}": "data:image/jpeg;base64,${b64}",\n`;
}

const out = `/** Auto-generated by scripts/gen-menu-inline.mjs — do not edit by hand. */
export const MENU_INLINE_IMAGE_BY_ID: Record<string, string> = {
${body}};
`;

const outPath = path.join(root, "src", "lib", "menu-inline-data.ts");
fs.writeFileSync(outPath, out, "utf8");
console.log("Wrote", outPath, fs.statSync(outPath).size, "bytes");
